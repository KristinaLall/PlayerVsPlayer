
//aggregate class Weapon
public class Weapon {
	
	//Declaring variables
	private String name;
	private int cost;
	private int damageAmount;

	//setters and getters for weapon name
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	//setters and getters for weapon cost
	public int getCost() {
		return cost;
	}
	public void setCost(int cost) {
		this.cost = cost;
	}
	
	//setters and getters for weapon damage amount
	public int getDamageAmount() {
		return damageAmount;
	}
	public void setDamageAmount(int damageAmount) {
		this.damageAmount = damageAmount;
	}

	
}//end class Weapon
